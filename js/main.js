let currentImageIndex = 0;
let currentLang = 'id';

// DOM Elements
const navbar = document.getElementById('navbar');
const hamburger = document.getElementById('hamburger');
const navLinks = document.getElementById('navLinks');
const langToggle = document.getElementById('langToggle');
const currentLangEl = document.getElementById('currentLang');
const otherLangEl = document.getElementById('otherLang');
const lightbox = document.getElementById('lightbox');
const lightboxImg = document.getElementById('lightbox-img');
const lightboxCaption = document.getElementById('lightbox-caption');
const contactForm = document.getElementById('contactForm');
const filterBtns = document.querySelectorAll('.filter-btn');
const projectCards = document.querySelectorAll('.project-card');

// Initialize
document.addEventListener('DOMContentLoaded', () => {
    const storedLang = localStorage.getItem('ymt-lang');
    if (storedLang) {
        currentLang = storedLang;
        updateLanguage();
    }
    handleScroll();
});

// Navbar scroll effect
window.addEventListener('scroll', handleScroll);

function handleScroll() {
    if (window.scrollY > 80) {
        navbar.classList.add('scrolled');
    } else {
        navbar.classList.remove('scrolled');
    }
}

// Mobile menu toggle
hamburger.addEventListener('click', () => {
    hamburger.classList.toggle('active');
    navLinks.classList.toggle('active');
});

// Close mobile menu when clicking a link
document.querySelectorAll('.nav-links a').forEach(link => {
    link.addEventListener('click', () => {
        hamburger.classList.remove('active');
        navLinks.classList.remove('active');
    });
});

// Language toggle
langToggle.addEventListener('click', () => {
    currentLang = currentLang === 'id' ? 'en' : 'id';
    localStorage.setItem('ymt-lang', currentLang);
    updateLanguage();
});

function updateLanguage() {
    currentLangEl.textContent = currentLang.toUpperCase();
    otherLangEl.textContent = currentLang === 'id' ? 'EN' : 'ID';

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[currentLang][key]) {
            el.innerHTML = translations[currentLang][key];
        }
    });
}

// Project filter
filterBtns.forEach(btn => {
    btn.addEventListener('click', () => {
        const filter = btn.getAttribute('data-filter');

        filterBtns.forEach(b => b.classList.remove('active'));
        btn.classList.add('active');

        projectCards.forEach(card => {
            const category = card.getAttribute('data-category');
            if (filter === 'all' || category === filter) {
                card.classList.remove('hidden');
            } else {
                card.classList.add('hidden');
            }
        });
    });
});

// Lightbox functions
function openLightbox(index) {
    currentImageIndex = index;
    lightboxImg.src = lightboxData[currentImageIndex].src;
    lightboxCaption.textContent = lightboxData[currentImageIndex].caption;
    lightbox.classList.add('active');
    document.body.style.overflow = 'hidden';
}

function closeLightbox() {
    lightbox.classList.remove('active');
    document.body.style.overflow = '';
}

function prevImage() {
    currentImageIndex = (currentImageIndex - 1 + lightboxData.length) % lightboxData.length;
    lightboxImg.src = lightboxData[currentImageIndex].src;
    lightboxCaption.textContent = lightboxData[currentImageIndex].caption;
}

function nextImage() {
    currentImageIndex = (currentImageIndex + 1) % lightboxData.length;
    lightboxImg.src = lightboxData[currentImageIndex].src;
    lightboxCaption.textContent = lightboxData[currentImageIndex].caption;
}

// Lightbox keyboard navigation
document.addEventListener('keydown', (e) => {
    if (!lightbox.classList.contains('active')) return;

    if (e.key === 'Escape') closeLightbox();
    if (e.key === 'ArrowLeft') prevImage();
    if (e.key === 'ArrowRight') nextImage();
});

// Close lightbox on background click
lightbox.addEventListener('click', (e) => {
    if (e.target === lightbox) {
        closeLightbox();
    }
});

// Contact form handling
contactForm.addEventListener('submit', (e) => {
    e.preventDefault();

    const name = document.getElementById('name').value;
    const email = document.getElementById('email').value;
    const phone = document.getElementById('phone').value;
    const message = document.getElementById('message').value;

    const subject = encodeURIComponent(currentLang === 'id' ? 'Kontak Website YMT' : 'YMT Website Contact');
    const mailtoLink = `mailto:yudhistiramt01@gmail.com?subject=${subject}&body=${encodeURIComponent(
        (currentLang === 'id' ? 'Nama: ' : 'Name: ') + name + '%0A' +
        'Email: ' + email + '%0A' +
        (currentLang === 'id' ? 'Telepon: ' : 'Phone: ') + phone + '%0A%0A' +
        (currentLang === 'id' ? 'Pesan:%0A' : 'Message:%0A') + message
    )}`;

    window.location.href = mailtoLink;

    alert(currentLang === 'id'
        ? 'Terima kasih! Email client Anda akan terbuka.'
        : 'Thank you! Your email client will open.');

    contactForm.reset();
});

// Smooth scroll for anchor links
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function(e) {
        e.preventDefault();
        const target = document.querySelector(this.getAttribute('href'));
        if (target) {
            const headerOffset = 80;
            const elementPosition = target.getBoundingClientRect().top;
            const offsetPosition = elementPosition + window.pageYOffset - headerOffset;

            window.scrollTo({
                top: offsetPosition,
                behavior: 'smooth'
            });
        }
    });
});